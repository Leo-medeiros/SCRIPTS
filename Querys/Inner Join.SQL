SET DATEFORMAT DMY;
SELECT b.ficha, b.seq, UPPER(A.NOME) NOME, CONVERT(CHAR(12),B.DTATENDE,103)+CONVERT(CHAR(5),B.DTATENDE,108) 'DT ATENDIMENTO',
B.MATRICULA, C.SIGLA 'CONVENIO', F.NOME 'ESPECIALIDADE',D.NOME 'MEDICO',CONVERT(CHAR(12),
E.SAIDA,103)+CONVERT(CHAR(5),E.SAIDA,108) + '/'+ E.OPERADOR 'TIPO DE ALTA', E.ORIGEM 'ORIGEM', '' AS 'EXAME', A.TEL 'TELEFONE', A.UF 'UF', RTRIM(A.NOMEMUN) 'MUNICIPIO',
B.RESPONSAVEL 'RESPONSAVEL', '' AS'GRAU DE PARENTESCO'
FROM RECEPCAO.DBO.PACIENTES A 
INNER JOIN RECEPCAO.DBO.MOVIMENTOS B ON A.CODIGO=B.CODPAC
INNER JOIN RECEPCAO.DBO.CONVENIOS C ON B.CONVENIO = C.CODIGO
INNER JOIN RECEPCAO.DBO.MEDICOS D ON B.CODMED = D.CODIGO
INNER JOIN RECEPCAO.DBO.REPOUSO E ON B.FICHA = E.FICHA
LEFT JOIN RECEPCAO.DBO.ESPECIALIDADES F ON B.ESPECIALIDADE = F.CODIGO
WHERE B.MOVIMENTO IN (1,2,3)
AND   B.DTATENDE BETWEEN '04/05/2015 00:00' AND '10/05/2015 23:59'
ORDER BY B.FICHA


