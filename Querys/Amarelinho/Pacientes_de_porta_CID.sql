WHERE YEAR(A.DTATENDE)='2015' AND MONTH(A.DTATENDE)='04' --MES DE 04/2015
AND A.MOVIMENTO IN(1,2,3,4)  --INFORMAR OS CÓDIGOS DE ATENDIMENTO PARA PS
AND P.dtnasc >'30/04/2000 00:00'
