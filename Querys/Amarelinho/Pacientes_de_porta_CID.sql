SELECT P.Nome, P.PRONTUARIO,A.FICHA, S.CID_CODIGO CID, recepcao.dbo.calcula_idade_completa(P.dtnasc) idadepaciente 
FROM RECEPCAO.DBO.MOVIMENTOS A  
