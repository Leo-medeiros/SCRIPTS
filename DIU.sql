SELECT AA.NOME, AA.NOMEEQUIPE, AA.IDADE, AA.MAE, AA.PRONTUARIO, AA.CNS, CONVERT (VARCHAR (20), BB.DATAFINALIZA, 103) DATAINSERCAO
FROM RECEPCAO.INDICADORES.VW_PEP_SIAB AA
INNER JOIN RECEPCAO.DBO.PEPMOVIMENTOS BB ON AA.IDPEP=BB.CODPAC
INNER JOIN RECEPCAO.DBO.PEPMOVIMENTOS_SOLI_PROCEDIMENTO CC ON BB.CODIGO=CC.ID_PEPMOVIMENTOS
INNER JOIN RECEPCAO.DBO.PROCEDIMENTOS DD ON CC.ID_EXAME=DD.ID
WHERE DD.CODPROC IN ('0301040028') 

