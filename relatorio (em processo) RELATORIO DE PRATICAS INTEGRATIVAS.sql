inner join RECEPCAO.dbo.medicos m on (p.codmed = m.CODIGO)
inner join RECEPCAO.Indicadores.vw_pep_siab vw on (p.codpac = vw.IDPep)
inner join RECEPCAO.dbo.PepMovimentos_Soli_Procedimento s on (p.Codigo = s.id_PepMovimentos)
inner join RECEPCAO.dbo.Procedimentos pr on (pr.CODIGO = s.id_PepMovimentos_Soli_Procedimento)

select CODPROC,* from RECEPCAO.dbo.Procedimentos
where CODPROC in ('0101050089', '0101050062', '0101050070', '0101050011',
